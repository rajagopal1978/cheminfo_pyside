    @classmethod
    def get_theme(cls, theme_name="scientific_blue"):
        """Get a specific theme by name"""
        themes = cls.get_all_themes()
        return themes.get(theme_name.lower(), cls.SCIENTIFIC_BLUE)

    @classmethod
    def list_themes(cls):
        """List all available themes with descriptions"""
        themes = cls.get_all_themes()
        print("\n" + "="*70)
        print("Available Color Themes for RDKit Tools Application")
        print("="*70 + "\n")

        for i, (key, theme) in enumerate(themes.items(), 1):
            print(f"{i:2d}. {theme['name']}")
            print(f"    Key: {key}")
            print(f"    {theme['description']}")
            print(f"    Primary: {theme['primary']} | Accent: {theme['accent']}")
            print()


